/**
 * Simple launcher for the JavaFX Chess application.
 * This class serves as a bridge between the Main class and ChessApp.
 */
public class ChessAppLauncher {
    /**
     * Main method that launches the JavaFX Chess application.
     * 
     * @param args Command-line arguments to pass to the JavaFX application
     */
    public static void main(String[] args) {
        ChessApp.main(args);
    }
}
